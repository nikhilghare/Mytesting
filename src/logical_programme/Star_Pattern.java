package logical_programme;

import java.util.Scanner;

public class Star_Pattern {

	public static void main(String[] args) {
		for(int i=1;i<=4;i++)// for loop == for  no. of rows
		    
		{
			for(int j=1;j<=4;j++) // for loop == for no. of columns 
			{

			System.out.print(" *");// to print * do not use println
			}
		     
		System.out.println("");// to print * on next row

		
	}

	}
}

